// Binary

// Assignment                :- =
// Addition                  :- +
// Subtraction               :- -
// Multiplication            :- *
// Division                  :- /
// Modulus (remainder)       :- %

public class arithmetic_operators1{
    public static void main (String args[]){

        int a = 21;
        int b = 3;

        System.out.println("1st no.:- "+ a + " \n2nd no.:- " + b );
        

        System.out.println("Addition of two nos:- " + (a+b));  //  add

        System.out.println("Subtraction of two nos:- "+ (a-b)); // subtract

        System.out.println("Multiplication of two nos:- "+ (a*b)); // multiply

        System.out.println("Division of two nos:- " + (a/b)); // Division

        System.out.println("Modulus of two nos:- " + (a%b));  // Modulus



    
    }
}